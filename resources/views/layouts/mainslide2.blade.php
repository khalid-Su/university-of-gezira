                            environment, without negligence of the urban environment, to identify issues and conduct
                            research on them. This approach calls for the principle of "unity of science", i.e.,
                            science, art and knowledge are varied important subjects but closely linked with each other
                            and overlapping to the point that makes them coherent and integrated. As the rural community
                            looks to its issues in a complementary manner, the University of Gezira has adopted this
                            approach to achieve sustainable economic and social development in the community as a whole
                            and especially rural areas. As the University of Gezira has aimed to study the environment
                            in its broad sense, it is natural that the university tends to adopt integration of
                            knowledge in order to improve the link between education and environment and integrate the
                            perception of the overall developmental programs. Therefore, the university is charged with
                            a threefold mission: Cognitive, Environmental, and social mission.</p>
